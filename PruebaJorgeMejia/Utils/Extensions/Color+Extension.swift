import Foundation
import SwiftUI

extension Color {
    static let background = Color.init(red: 0.1, green: 0.1, blue: 0.1)
    static let primary = Color.init(red: 1, green: 1, blue: 1)
    static let secondary = Color.init(red: 1, green: 0.7, blue: 0)
    
    static let buttonBackground = Color.primary.opacity(0.1)
}
